
package ex01;

public class Ex01 {

    public static void main(String[] args) {
        double m1 = (8+7+9)/3;
        double m2 = (4+5+6)/3;
        double somaM = m1+m2;
        double mediaM = (m1+m2)/2;
        
        System.out.println("Média de 7,8 e 9: " + m1);
        System.out.println("Média de 4,5 e 6: " + m2);
        System.out.println("Soma das Médias: " + somaM);
        System.out.println("Média das Médias: " + mediaM);
        
    }
    
}
